function generateChartData(bazi: BaziResult, dm: DayMasterAnalysis) {
  const ec = dm.elementCounts;
  const elementDistribution = Object.entries(ec).map(([name, value]) => ({
    name,
    value: Math.round(value * 10) / 10,
    fill: ELEMENT_COLORS[name as WuXing] || '#666',
  }));

  const shishenCounts: Record<string, number> = {};
  const allStems = [bazi.year.stem, bazi.month.stem, bazi.day.stem, bazi.hour.stem];
  for (const stem of allStems) {
    if (stem === bazi.dayMaster) continue;
    const ss = getShiShen(bazi.dayMaster, stem);
    shishenCounts[ss] = (shishenCounts[ss] || 0) + 1;
  }
  const shishenDistribution = Object.entries(shishenCounts).map(([name, value]) => ({ name, value }));

  return {
    elementDistribution,
    shishenDistribution,
    strengthGauge: { score: dm.score, label: dm.strength },
  };
}

